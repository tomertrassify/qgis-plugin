def tr(language: str | None, key: str, **kwargs) -> str:
    normalized = normalize_ui_language(language)
    template = (
        TRANSLATIONS.get(normalized, {}).get(key)
        or TRANSLATIONS[DEFAULT_UI_LANGUAGE].get(key)
        or key
    )
    try:
        return template.format(**kwargs)
    except Exception:
        return template


def localized_value(data: dict | None, language: str | None, key: str, fallback: str = "") -> str:
    source = dict(data or {})
    normalized = normalize_ui_language(language)

    candidate_keys = []
    if normalized == "en":
        candidate_keys.extend((f"{key}_en", f"{key}_de", key))
    else:
        candidate_keys.extend((f"{key}_de", key, f"{key}_en"))

    for candidate_key in candidate_keys:
        value = str(source.get(candidate_key, "") or "").strip()
        if value:
            return value
    return str(fallback or "").strip()
